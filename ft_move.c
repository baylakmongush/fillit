#include "fillit.h"

void		ft_move(int *coord_of_tet, int nbr_tet, char x_y)
{
	int	i;

	i = (x_y == 'x') ? ((nbr_tet - 1) * 8) : ((nbr_tet - 1) * 8 + 1);
	while (i < nbr_tet * 8)
	{
		coord_of_tet[i] += 1;
		i += 2;
	}
}
